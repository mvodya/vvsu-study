# task7.py
x = 10
y = 3

print("Сложение:", x + y)
print("Вычитание:", x - y)
print("Умножение:", x * y)
print("Деление:", x / y)
print("Целочисленное деление:", x // y)
print("Остаток от деления:", x % y)
print("Возведение в степень:", x ** y)

print("Равно:", x == y)
print("Не равно:", x != y)
print("Больше:", x > y)
print("Меньше:", x < y)
print("Больше или равно:", x >= y)
print("Меньше или равно:", x <= y)
